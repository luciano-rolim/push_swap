#include "libft.h"

long int	ft_atol(const char *nptr)
{
	long int	i;
	long int	np;
	long int	number;

	if (!nptr)
		return (0);
	i = 0;
	np = 1;
	number = 0;
	while ((nptr[i] >= 9 && nptr[i] <= 13) || nptr[i] == 32)
		i++;
	if (nptr[i] == '+' || nptr[i] == '-')
	{
		if (nptr[i] == '-')
			np *= -1;
		i++;
	}
	while (nptr[i] >= '0' && nptr[i] <= '9')
	{
		number = number * 10 + nptr[i] - '0';
		i++;
	}
	return (number * np);
}
